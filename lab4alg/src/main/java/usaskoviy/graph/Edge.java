package usaskoviy.graph;

/**
 * Элементарное ребро графа: упорядоченная пара индексов вершин.
 *
 * В ориентированном графе пара читается как дуга из {@code from} в {@code to}.
 * В неориентированном графе при нормализации в {@link Graph} ребро может быть приведено
 * к виду (меньший индекс, больший), чтобы не дублировать одно и то же ребро.
 *
 * Тип {@code record} в Java даёт неизменяемость, {@code equals}/{@code hashCode}, геттеры {@code from()} и {@code to()}.
 */
public record Edge(int from, int to) {
    /**
     * Компактный конструктор record: проверка до присвоения полям.
     * Отрицательные индексы запрещены — вершины у нас только 0 … n−1.
     */
    public Edge {
        if (from < 0 || to < 0) {
            throw new IllegalArgumentException("Индексы вершин не могут быть отрицательными");
        }
    }
}
