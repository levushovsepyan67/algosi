package usaskoviy.tree; // пакет с реализацией структур данных для ЛР6

/**
 * Бинарное дерево поиска (BST): вставка, поиск и удаление выполняются итеративно,
 * чтобы при вырождении дерева в цепочку (отсортированные ключи) не переполнить стек JVM.
 */
public final class BinarySearchTree {

    private Node root; // ссылка на корень дерева; null — дерево пустое

    /** Узел BST: ключ и два потомка (левый — меньше ключа, правый — больше). */
    private static final class Node {
        int key; // значение ключа в узле
        Node left; // левое поддерево: все ключи строго меньше key
        Node right; // правое поддерево: все ключи строго больше key

        Node(int key) {
            this.key = key; // сохраняем переданный ключ в новом узле
        }
    }

    /** Обнуляет корень — дерево становится пустым (узлы соберёт сборщик мусора). */
    public void clear() {
        root = null; // отвязываем корень от структуры
    }

    /**
     * Вставка ключа: спуск от корня без рекурсии.
     * Дубликат ключа игнорируется (дерево не меняется).
     */
    public void insert(int key) {
        if (root == null) { // пустое дерево — новый узел становится корнем
            root = new Node(key); // создаём корневой узел с данным ключом
            return; // вставка завершена
        }
        Node cur = root; // текущий узел при спуске, начинаем с корня
        while (true) { // цикл до успешной вставки
            if (key < cur.key) { // ищем место в левом поддереве
                if (cur.left == null) { // свободное место слева найдено
                    cur.left = new Node(key); // вешаем новый лист слева
                    return; // вставка выполнена
                }
                cur = cur.left; // идём глубже влево
            } else if (key > cur.key) { // ищем место в правом поддереве
                if (cur.right == null) { // свободное место справа найдено
                    cur.right = new Node(key); // вешаем новый лист справа
                    return; // вставка выполнена
                }
                cur = cur.right; // идём глубже вправо
            } else {
                return; // ключ уже есть — дубликат не добавляем
            }
        }
    }

    /** Поиск ключа: итеративный спуск, как при вставке. Возвращает true, если ключ найден. */
    public boolean contains(int key) {
        Node cur = root; // начинаем с корня
        while (cur != null) { // пока не вышли за пределы дерева
            if (key < cur.key) {
                cur = cur.left; // ключ меньше — влево
            } else if (key > cur.key) {
                cur = cur.right; // ключ больше — вправо
            } else {
                return true; // равенство — найдено
            }
        }
        return false; // дошли до null — ключа нет
    }

    /**
     * Удаление узла с данным ключом (итеративно).
     * Если ключа нет — дерево не меняется.
     * Два ребёнка: копируем ключ минимального узла правого поддерева, затем удаляем тот узел.
     */
    public void delete(int key) {
        Node parent = null; // родитель текущего узла (для переподвешивания ссылок)
        Node cur = root; // с какого узла начинаем поиск удаляемого
        while (cur != null && cur.key != key) { // ищем узел с нужным ключом
            parent = cur; // запоминаем родителя перед переходом вниз
            if (key < cur.key) {
                cur = cur.left; // идём влево
            } else {
                cur = cur.right; // идём вправо
            }
        }
        if (cur == null) {
            return; // ключ не найден — нечего удалять
        }
        if (cur.left != null && cur.right != null) { // два ребёнка — нужен преемник по значению
            Node succParent = cur; // родитель узла-преемника (минимум в правом поддереве)
            Node succ = cur.right; // минимум правого поддерева — самый левый в нём
            while (succ.left != null) { // идём влево до конца в правом поддереве
                succParent = succ; // обновляем родителя
                succ = succ.left; // следующий левый
            }
            cur.key = succ.key; // копируем ключ преемника в удаляемую позицию
            cur = succ; // дальше удаляем узел-преемник (у него не более одного ребёнка)
            parent = succParent; // родитель для финального шага — родитель преемника
        }
        Node child = cur.left != null ? cur.left : cur.right; // единственный ребёнок или null
        if (parent == null) {
            root = child; // удаляем корень — новый корень = ребёнок
        } else if (parent.left == cur) {
            parent.left = child; // cur был левым ребёнком
        } else {
            parent.right = child; // cur был правым ребёнком
        }
    }
}
